    for inst in code:
        pythonCode += '    ' * level
        pythonCode += functions[inst]
        if inst == '[':
            level += 1
        elif inst == ']':
            level -= 1
        pythonCode += '\n'
    pythonCode = optimize(pythonCode)
    f = open('C:\\temp\\lol.txt', 'w')
    f.write(pythonCode)
